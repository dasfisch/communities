					<h1 class="content-headline">8 Tips for Installing a Washing Machine</h1>
				
					<p>
						So, you bought a <a target="_blank" href="http://www.sears.com/appliances-washers/c-1101269">new washing machine </a>and you’re ready to install it and put it right to work. Before you do, make sure your laundry room’s floor and pipes are ready too.
					</p>
					
					<h2>A Stable Floor</h2>
					<p>
						Whatever washing machine you choose, the water’s stop-and-go action generates a lot of force at the machine’s feet. That force transfers to the floor beneath, which is why washers were traditionally installed on strong, force-dampening concrete basement floors. Today’s laundry rooms, however, are often upstairs where wood subfloor and joists have to bear the force. You can still have a second-floor laundry center, but check the floor structure first:
					</p>
					<ol class="bullets">
						<li>It should be extra stiff to resist vibration. If the floor is weak or spongy, the washer won’t operate properly. Adding thicker plywood subfloor or blocking helps.</li>
						<li>If you have a tile floor, install an extra-thick mortar base beneath it.</li>
						<li>Your floor covering matters too; if it’s not abrasion resistant, the floor could suffer. The feet of an aggressively bouncing machine with an unbalanced load can rip apart sheet vinyl floors.</li>
					</ol>
					
					<h2>Plumbing</h2>
					<p>
						Washers have simple plumbing, but that doesn’t mean the components aren’t important. Proper <a target="_blank" href="http://www.sears.com/tools-plumbing-tools/c-1221021417">plumbing</a> is key to a washer’s functionality. Don’t hurry through installation or brush off small problems &mdash; you could end up with a huge mess.
					</p>
					<ol class="bullets">
						<li>Most washers come with relatively low-quality supply hoses that can swell under pressure and then burst. Replace them with braided steel hoses to reduce the risk of catastrophic leaks.</li>
						<li>If it’s hard to reach the shut-off faucets for the hoses, install faucets within reach so you can easily control the water. Then get in the habit of shutting off the water when you’re not doing laundry.</li>
						<li>Inadequately supported supply pipes can rattle as the water turns on and shuts off during the wash cycle. Keep them quiet by clamping the pipes securely to the wall or to one another &mdash; just don’t clamp them so tightly that they can’t expand and contract as the water temperature changes. Wrap the <a target="_blank" href="http://www.sears.com/tools-hand-tools-clamps/s-1028720">clamps</a> with electrical tape to stop them from grabbing the pipes during these fluctuations, or place a small piece of insulation between the clamp and the pipe.</li>
						<li>Install a drain in the floor of all laundry rooms above basement level. If you can’t install a drain, place a tray or pan below the machine to catch leaks. The best of both worlds is a tray with an integral drain line.</li>
						<li>Used wash water usually drains into a vertical drain called a standpipe. Connect the pipe before the trap to keep sewer odors out of the standpipe. The drainpipe should be less than 8-ft. tall so water drains easily.</li>
					</ol>
